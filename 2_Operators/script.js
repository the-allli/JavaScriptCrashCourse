// Arithmetic Operators
// These perform mathematical calculations:
// + Addition (also string concatenation)
// - Subtraction
// * Multiplication
// / Division
// % Modulus (remainder)
// ** Exponentiation
// ++ Increment (prefix/postfix)
// -- Decrement (prefix/postfix)


// Assignment Operators
// These assign values to variables:
// = Basic assignment
// += Addition assignment
// -= Subtraction assignment
// *= Multiplication assignment
// /= Division assignment
// %= Modulus assignment
// **= Exponentiation assignment
// &=, |=, ^= Bitwise assignment operators
// <<=, >>=, >>>= Bitwise shift assignment


// Comparison Operators
// These compare values and return boolean results:
// == Equality (with type coercion)
// === Strict equality (no type coercion)
// != Inequality (with type coercion)
// !== Strict inequality (no type coercion)
// < Less than
// > Greater than
// <= Less than or equal
// >= Greater than or equal


// Logical Operators
// These perform logical operations:
// && Logical AND
// || Logical OR
// ! Logical NOT
// ?? Nullish coalescing (returns right operand if left is null/undefined)


// Bitwise Operators
// These operate on binary representations:
// & Bitwise AND
// | Bitwise OR
// ^ Bitwise XOR
// ~ Bitwise NOT
// << Left shift
// >> Right shift
// >>> Unsigned right shift


// String Operators
// + Concatenation
// += Concatenation assignment


// Conditional (Ternary) Operator
// A shorthand for if-else statements:
// condition ? expressionIfTrue : expressionIfFalse;


// Type Operators
// typeof Returns the type of a variable
// instanceof Tests if an object is an instance of a constructor


// Unary Operators
// Operators that work with a single operand:
// + Unary plus (converts to number)
// - Unary minus (negates)
// ! Logical NOT
// ~ Bitwise NOT
// typeof Type checking
// void Evaluates expression and returns undefined
// delete Deletes object properties


// Spread and Rest Operators
// ... Spread operator (expands arrays/objects)
// ... Rest operator (collects remaining elements)


// Optional Chaining and Nullish Coalescing
// Modern operators for safer property access:
// ?. Optional chaining
// ?? Nullish coalescing
// ??= Nullish coalescing assignment


// Destructuring Assignment
// Extracts values from arrays or objects:
// javascript// Array destructuring
// let [a, b] = [1, 2];
// // Object destructuring
// let {name, age} = {name: "John", age: 30};